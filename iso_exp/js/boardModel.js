
function GameBoard(){
      this.board = [];
}

GameBoard.prototype.generateBoard = function(){
      var board = [];
      for(var x = 101; x >= 1 ; --x){
            var row = [];
            for(var y = 101; y >= 1; --y){
                 var tileNum = Math.floor(Math.random() * 2);
                 row.push(tileNum);
            }
            board.push(row);
      }
      return board;
}
